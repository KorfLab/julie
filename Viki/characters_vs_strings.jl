# Using single quotes results in a character
a = 'b'
println(a)

# Using double quotes results in a string
a = "b"
println(a)


# On the command line, using the command a = 'b' results in the output
# 'b': ASCII/Unicode U+0062 (category Ll: Letter, lowercase)

# Using the command a = "b" results in the output
# "b"